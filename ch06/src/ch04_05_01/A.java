package ch04_05_01;

/**
 * 이름이 A 이고 String 타입의 field1이라는 필드를 가진 클래스,
   A클래스를 생성시(new A("hello");) field1의 초기값을 "hello"로 초기화 하도록
클래스를 만드시오.
 * */
//클래스의 맴버
public class A {
	//필드
   String field1;
   
   //생성자
   //매개변수 1개짜리 생성자 - 필드 초기화
   A(String hello){ field1=hello;}
   
   //메소드
   
}
